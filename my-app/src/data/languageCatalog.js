export const popularLanguageCatalog = languageCatalog.filter((language) => language.popular);

function normalizeForSearch(value) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();
}

function scoreMatch(language, query) {
  if (!query) return language.popular ? 100 : 50;

  const fields = [
    language.code,
    language.nativeName,
    language.englishName,
    ...(language.aliases || []),
  ];

  let best = 0;
  for (const field of fields) {
    const normalized = normalizeForSearch(field);
    if (!normalized) continue;
    if (normalized === query) best = Math.max(best, 130);
    else if (normalized.startsWith(query)) best = Math.max(best, 100);
    else if (normalized.includes(query)) best = Math.max(best, 70);
  }
  return best;
}

export function searchLanguageCatalog(query) {
  const normalizedQuery = normalizeForSearch(query || "");
  return languageCatalog
    .map((language) => ({ language, score: scoreMatch(language, normalizedQuery) }))
    .filter(({ score }) => score > 0)
    .sort((a, b) => {
      if (b.score !== a.score) return b.score - a.score;
      if (a.language.popular !== b.language.popular) return a.language.popular ? -1 : 1;
      return a.language.englishName.localeCompare(b.language.englishName);
    })
    .map(({ language }) => language);
}
